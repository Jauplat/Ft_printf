#include "libft.h"

void	ft_putnbr(intmax_t n)
{
	if (n < 10)
		ft_putchar((n % 10) + 48);
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putchar((n % 10) + 48);
	}
}
