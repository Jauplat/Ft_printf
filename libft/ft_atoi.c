#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int nbr;

	nbr = 0;
	i = 0;
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v') ||
			(str[i] == ' ') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	while ((str[i] == '+' || str[i] == '-' || str[i] == ' ' || \
		str[i] == '0' || str[i] == '#') && str[i] != '\0')
		i++;
	while (str[i] && ('9' >= str[i] && str[i] >= '0'))
	{
		nbr *= 10;
		nbr += str[i] - '0';
		i++;
	}
	return (nbr);
}
